def parse_response(results, ragam, data):
    for item in data["items"]:
        ragam_mod = ragam
        if ragam.endswith("ragam"):
            ragam_mod = ragam[:-6]

        record = {'ragam': ragam_mod,
                  'video_id': item["id"]["videoId"], 
                  'title': item["snippet"]["title"], 
                  'desc': item["snippet"]["description"]}
        results.append(record)

    return results

def call_youtube():
    results = []
    ragas_full = list_ragas()

    for raga in ragas_full:
        params = {
            "part": "snippet",
            "maxResults": 10,
            "q": raga,
            "type": "video",
            "key": API_KEY
        }
    
        response = requests.get(url, params)

        if not response.status_code == 200:
            print(response)
        else:
            results = parse_response(results, raga, response.json())
    
    if len(results) > 0:
        df = pd.DataFrame(results)
        df.to_csv('youtubeurls.csv', index = False)

